package Day04_Concatenation;

public class PrimitiveTypeCastings {
    public static void main(String[] args) {

        byte a =15;
        short b=(short)a;
        double n1= 2.5;
        byte n2= (byte) n1;
        int num= 500;
        byte result = (byte)num;



        System.out.println(b);
        System.out.println(n2);
        System.out.println(result);

    }


}
