package Day06_IfStatement;

public class SingleIfStatementPractice {
    public static void main(String[] args) {
        int number = 100;
        boolean isEvenNumber = number % 2 == 0;
        if (isEvenNumber) {
            System.out.println("Even number");
        }
        if (!isEvenNumber) {
            System.out.println("Odd number");


        }






    }
}